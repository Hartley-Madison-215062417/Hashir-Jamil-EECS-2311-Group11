package tab2mxl;

public class Drums {
	
	public String instrumentID(String instrument) {
		String instID = " ";
		if (instrument == "CC || C"){
				instID = "Crash Symbal";
		}else if(instrument == "R || Rd") {
			instID = "Ride Cymbal";
		}else if(instrument == "HH || H") {
			instID = "P1-I43";
		}else if(instrument == "HF") {
			instrument = "Closed Hi-Hat";
		}
		 

		return inst;
	}
	
	

}
