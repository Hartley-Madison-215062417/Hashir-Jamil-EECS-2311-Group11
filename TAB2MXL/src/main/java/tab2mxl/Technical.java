package tab2mxl;

public class Technical {
	private int string;
	private int fret;
	
	public Technical(int stringNum, int fretNum) {
		this.string = stringNum;
		this.fret = fretNum;
	}

	public int getString() {
		return string;
	}

	public void setString(int string) {
		this.string = string;
	}

	public int getFret() {
		return fret;
	}

	public void setFret(int fret) {
		this.fret = fret;
	}
	
	

}
